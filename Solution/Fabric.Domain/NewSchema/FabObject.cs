namespace Fabric.Domain.NewSchema {

	/*================================================================================================*/
	public abstract class FabObject {


		////////////////////////////////////////////////////////////////////////////////////////////////
		/*--------------------------------------------------------------------------------------------*/
		public virtual void Fill(Vertex pVertex) {}


		////////////////////////////////////////////////////////////////////////////////////////////////
		/*--------------------------------------------------------------------------------------------*/
		protected static void SetApiValue<T>(T pDomain, out T pApi) {
			pApi = pDomain;
		}

		/*--------------------------------------------------------------------------------------------*/
		protected static void SetDomainValue<T>(T pApi, out T pDomain) {
			pDomain = pApi;
		}

	}

}