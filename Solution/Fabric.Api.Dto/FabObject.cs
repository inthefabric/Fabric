using Fabric.Infrastructure.Db;

namespace Fabric.Api.Dto {

	/*================================================================================================*/
	public abstract class FabObject : IFabObject {

		public string Dto { get; set; }


		////////////////////////////////////////////////////////////////////////////////////////////////
		/*--------------------------------------------------------------------------------------------*/
		protected FabObject() {
			Dto = GetType().Name;
		}

		/*--------------------------------------------------------------------------------------------*/
		public abstract void Fill(IDbDto pDbDto);

	}

}